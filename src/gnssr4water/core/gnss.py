from collections import namedtuple
from scipy.constants import c

GNSSfreq=namedtuple("GNSSfreq",["freq","length","width","name"])

def getGNSS(freqMhz,wMhz,name):
    freq=freqMhz*1e6
    return GNSSfreq(freq,c/freq,wMhz*1e6,name)

